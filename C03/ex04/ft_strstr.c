char	*ft_strstr(char *str, char *to_find)
{
	int i;
	int y;

	i = 0;
	y = 0;
	while(str[i] != '\0')
	{
		if(str[i] == str[y])
		{
			while(to_find[y] != '\0')
			{
				i++;
				y++;
			}
		}
		else
		{
			i++;
		}
	}
}