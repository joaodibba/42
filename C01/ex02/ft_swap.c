#include <stdio.h>

int	mai(int *a, int *b)
{
	int	troca;

	*a = 42;
	*b = 43;
	troca = *a;
	*a = *b;
	*b = troca;
	printf("%d\n", *a);
	printf("%d\n", *b);
	return (0);
}
